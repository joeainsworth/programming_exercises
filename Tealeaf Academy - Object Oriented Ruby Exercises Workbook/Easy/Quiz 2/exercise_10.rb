# What are the benefits of using Object Oriented Programming in Ruby?
# Think of as many as you can.
#
# 1. Creating objects allows programmers to think more abstractly about the code
# they are writing.
# 2. Objects are represented by nouns so are easier to conceptualize.
# 3. It allows us to only expose functionality to the parts of code that need
# it, meaning namespace issues are much harder to come across
# 4. It allows us to easily give functionality to different parts of an
# application without duplication
# 5. We can build applications faster as we can reuse pre-writter code.
# 6. As the softwar becomes more complex this complexity can be easily managed.
